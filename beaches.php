	    <p class="text-justify">Sri Lanka is renowned for its stunning coastline, pristine beaches, and crystal-clear waters, making it a popular destination for beach lovers and water enthusiasts. The island's coastline stretches for about 1,340 kilometers (around 832 miles) and offers a diverse range of beach experiences.</p>
