	            <p class="card-text">Mirissa is the largest fishing port on the south coast and sits right on the southern tip of the island. Its beauty and relative proximity to Colombo (the capital city) mean it's not hard to access. It's about a two-hour drive from the capital's airport. The small beach town and its large beach are beautiful, backed by a dense grove of palm trees. The town is on a little hill that rises up behind the beach. Mirissa Beach is arrow-shaped, with the arrow tip sticking out into the sea. This natural jetty also makes the surf calm and good for swimming. The beaches are bright white, with soft, fine sand, and the water is crystal clear. It's light green where the bottom is sandy and dark blue where there are rocks or reef.</p>
